package com.ojas.rpo.security.util;

import java.util.List;

import com.ojas.rpo.security.entity.Skill;

public class RequirementDetails {
	private Long id;
	private String relavantExperience;
	private String totalExperience;
	private List<Skill> skills;
	//private boolean locations;
	private String expectedCTC;
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getRelavantExperience() {
		return relavantExperience;
	}
	public void setRelavantExperience(String relavantExperience) {
		this.relavantExperience = relavantExperience;
	}
	public String getTotalExperience() {
		return totalExperience;
	}
	public void setTotalExperience(String totalExperience) {
		this.totalExperience = totalExperience;
	}
	public List<Skill> getSkills() {
		return skills;
	}
	public void setSkills(List<Skill> skills) {
		this.skills = skills;
	}
	public String getExpectedCTC() {
		return expectedCTC;
	}
	public void setExpectedCTC(String expectedCTC) {
		this.expectedCTC = expectedCTC;
	}
	
	
}
