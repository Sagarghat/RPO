package com.ojas.rpo.security.entity;

import com.ojas.rpo.security.util.CandidateDetails;
import com.ojas.rpo.security.util.RequirementDetails;

public class CandidateRequirementMatcher {
	private boolean relavantExperience;
	private boolean totalExperience;
	private boolean skills;
	private boolean locations;
	private boolean expectedCTC;
	private CandidateDetails candidateDetails;
	private RequirementDetails requirementDetails;
	public boolean isExpectedCTC() {
		return expectedCTC;
	}
	public void setExpectedCTC(boolean expectedCTC) {
		this.expectedCTC = expectedCTC;
	}
	public boolean isRelavantExperience() {
		return relavantExperience;
	}
	public void setRelavantExperience(boolean relavantExperience) {
		this.relavantExperience = relavantExperience;
	}
	public boolean isTotalExperience() {
		return totalExperience;
	}
	public void setTotalExperience(boolean totalExperience) {
		this.totalExperience = totalExperience;
	}
	public boolean isSkills() {
		return skills;
	}
	public void setSkills(boolean skills) {
		this.skills = skills;
	}
	public boolean isLocations() {
		return locations;
	}
	public void setLocations(boolean locations) {
		this.locations = locations;
	}
	public CandidateDetails getCandidateDetails() {
		return candidateDetails;
	}
	public void setCandidateDetails(CandidateDetails candidateDetails) {
		this.candidateDetails = candidateDetails;
	}
	public RequirementDetails getRequirementDetails() {
		return requirementDetails;
	}
	public void setRequirementDetails(RequirementDetails requirementDetails) {
		this.requirementDetails = requirementDetails;
	}
	

}
